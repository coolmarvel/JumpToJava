package DataType;

public class NumberSample {
    public static void main(String[] args) {
        System.out.println(7 % 3); // 1
        System.out.println(3 % 7); // 3

        int i = 0;
        int j = 10;
        System.out.println(i); // 0
        i++;
        j--;
        System.out.println(i); // 1
        System.out.println(i++); // 1
        System.out.println(j); // 9
    }
}
